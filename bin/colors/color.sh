#!/bin/zsh

function tput_test(){
    for fg_ in {0..7}; do
        sf=$(tput setaf $fg_)
        for bg_ in {0..7}; do
            sb=$(tput setab $bg_)
            echo -n $sb$sf
            printf ' F:%s B:%s ' $fg_ $bg_
        done
        echo $(tput sgr0)
    done
}

function 256colors(){
    for code in {0..255}; do echo -e "\e[38;05;${code}m $code: Test"; done
}

function color8_init(){
    for i in {0..8}; do 
        printf -v f$i %b "\e[38;05;${i}m"
    done
    bld=$'\e[1m'
    rst=$'\e[0m'
    inv=$'\e[7m'
    w=$'\e[37m'
}

function init_ansi() {
    esc=""

    Bf="${esc}[30m";   rf="${esc}[31m";   gf="${esc}[32m"
    yf="${esc}[33m";   bf="${esc}[34m";   pf="${esc}[35m"
    cf="${esc}[36m";   wf="${esc}[37m";

