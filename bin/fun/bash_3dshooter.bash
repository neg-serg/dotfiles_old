OutOfRange(){
    #$1 = x, $2 = y
    if [[ $1 -lt 1 || $2 -lt 1 || $1 -gt $(( $LABIRINTH_HEIGHT - 2 )) || $2 -gt $(( $LABIRINTH_WIDTH - 2 )) ]]; then
        return 0
    fi
    return 1
}

CanGo(){
    #$1 = x, $2 = y
    if OutOfRange $1 $2; then
        return 1
    fi
    local -i r=$(( $1 * $LABIRINTH_WIDTH + $2 ))
    if [[ ${labirinth[r]} -eq 1 ]]; then
        return 1
    fi
    return 0
}

