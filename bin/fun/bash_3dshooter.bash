##================================================================Generate Lab Section=========================================================================

Check(){
    local -i x=$1
    local -i y=$2
    local -i dir=$3
    let "x += ${DIR_X[${dir}]}"
    let "y += ${DIR_Y[${dir}]}"
    if OutOfRange $x $y; then
        return 1
    fi
    if [[ ${labirinth[$(( $x * $LABIRINTH_WIDTH + $y ))]} -eq 0 ]]; then
        return 1
    fi
    if [[ ${labirinth[$(( (x + DIR_X[$dir]) * LABIRINTH_WIDTH + y + DIR_Y[$dir] )) ]} -eq 0 ]]; then
        return 1
    fi
    local -ai dx=(-1 -1 0 0)
    local -ai dy=(-1 0 -1 0)
    for (( i=0; i < 4; ++i )); do
        cnt=0
        for (( j=0; j < 4; ++j )); do
            let "cnt += ${labirinth[(x + dx[i] + j % 2) *  LABIRINTH_WIDTH + y + dy[i] + j / 2]}"
        done
        if [[ $cnt -eq 1 ]]; then
            return 1
        fi
    done
    return 0
}

Ended() {
    for (( ei=0; ei < 4; ++ei )); do
        if Check $1 $2 $ei; then
            return 1
        fi
    done
    return 0
}


GenerateLabirinth() {
    local -i x=$1
    local -i y=$2
    labirinth[$(( x * LABIRINTH_WIDTH + y ))]=0
    while ! `Ended $x $y`; do
        local -i dir=$(( $RANDOM % 4 ))
        while ! `Check $x $y $dir`; do
            dir=$(( $RANDOM % 4 ))
        done
        GenerateLabirinth $(( x + DIR_X[dir] )) $(( y + DIR_Y[dir] ))
    done
}

