PrintScreen() {
  #составление экрана для вывода
    local -ai rightWalls=()
    local -ai leftWalls=()
    local -i rightX=${DIR_X[$(( (playerDir + 1) % 4 ))]}
    local -i rightY=${DIR_Y[$(( (playerDir + 1) % 4 ))]}
    local -i frontWall=$MAX_SCREEN_DEPTH
    local -i monsterPos=$MAX_SCREEN_DEPTH
    local -i shotPos=$MAX_SCREEN_DEPTH
    echo -ne '\033[0;0f'
    for (( i=0; i < 8; ++i )); do
        x=$(( playerX + i * DIR_X[playerDir] ))
        y=$(( playerY + i * DIR_Y[playerDir] ))
        r=$(( x * LABIRINTH_WIDTH + y ))
        [[ $x -eq $monsterX && $y -eq $monsterY ]] && monsterPos=$i
        $shotExist && [[ $x -eq $shotX && $y -eq $shotY ]] && shotPos=$i
        if [[ ${labirinth[r]} -eq 1 ]]; then
            frontWall=$i
            break 1
        fi
        rightWalls[i]=${labirinth[$(( (x + rightX) * LABIRINTH_WIDTH + (y + rightY) ))]}
        leftWalls[i]=${labirinth[$(( (x - rightX) * LABIRINTH_WIDTH + (y - rightY) ))]}
    done

    local -a curScreen=()
    local -i r
    for (( x=0; x < $SCREEN_HEIGHT; ++x )); do
        for (( y=0; y < $SCREEN_WIDTH + 1; ++y )); do
            r=$(( $x * $SCREEN_WIDTH + $y))
            l=$(( $r + $x ))
            if [[ $y -eq $SCREEN_WIDTH ]]; then
                curScreen[$l]='\n'
            elif [[ $shotPos -lt $MAX_SCREEN_DEPTH && $shotPos -lt $monsterPos &&
                        $(( (x - SHOT_CENTER_X[$shotPos]) ** 2 + (y - SHOT_CENTER_Y) ** 2 )) -le $(( SHOT_RADIUS[$shotPos] ** 2 )) ]]; then
                curScreen[$l]=$SHOT_CHAR
            elif [[ $monsterPos -le MONSTER_FRONT[r] ]]; then
                curScreen[$l]=$MONSTER_CHAR
            elif [[ $frontWall -le $(( FRONT_WALLS[r] - 1 )) ]]; then
                curScreen[$l]=$FRONT_WALL_CHAR
            elif [[ ${WALLS[r]} -le 0 ]]; then
                if [[ $x -gt $CEIL_BOTTOM ]]; then
                    curScreen[$l]=$FLOOR_CHAR
                else
                    curScreen[$l]=$CEIL_CHAR
                fi
            elif [[ y -gt $(( SCREEN_WIDTH / 2 )) ]]; then #right
                if [[ ${rightWalls[$(( WALLS[r] - 1 ))]} -eq 0 ]]; then
                    if [[ ${EMPTY_WALLS[r]} -eq 0 ]]; then
                        if [[ $x -gt $CEIL_BOTTOM ]]; then
                            curScreen[$l]=$FLOOR_CHAR
                        else
                            curScreen[$l]=$CEIL_CHAR
                        fi
                    else
                        curScreen[$l]=$FRONT_WALL_CHAR
                    fi
                else
                    curScreen[$l]=$SIDE_WALL_CHAR     #${WALLS[r]}
                fi
            else                                          #left
                if [[ ${leftWalls[$(( WALLS[r] - 1 ))]} -eq 0 ]]; then
                    if [[ ${EMPTY_WALLS[r]} -eq 0 ]]; then
                        if [[ $x -gt $CEIL_BOTTOM ]]; then
                            curScreen[$l]=$FLOOR_CHAR
                        else
                            curScreen[$l]=$CEIL_CHAR
                        fi
                    else
                        curScreen[$l]=$FRONT_WALL_CHAR     #${WALLS[$r]}
                    fi
                else
                    curScreen[$l]=$SIDE_WALL_CHAR         #${WALLS[$r]}
                fi
            fi
        done
    done
    echo -en "${curScreen[*]}"
    DrawInterface
}


